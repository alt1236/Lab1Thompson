public class Wheels {

    /**
     * The size of the wheel.
     */
    private double size;

    /**
     * Constructs a Wheels object.
     *
     * @param size The wheel size.
     */
    public Wheels(double size) {
        this.size = size;
    }

    /**
     * Returns the wheel size.
     *
     * @return The wheel size.
     */
    public double getSize() {
        return size;
    }

    /**
     * Sets the wheel size.
     *
     * @param size The wheel size.
     */
    public void setSize(double size) {
        this.size = size;
    }
}
